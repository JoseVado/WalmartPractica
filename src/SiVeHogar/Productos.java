    private void btnModificarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnModificarMousePressed

        int idcell = jTable1.getSelectedRow();
        if (idcell <= -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe seleccionar el producto a editar. \n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        } else {
            List<Map<String, Object>> datos = dbManager.listar(TABLA);
            
            parent.actualizarPantalla(new UpProductos(this, datos.get(idcell)));
        }
    }//GEN-LAST:event_btnModificarMousePressed
