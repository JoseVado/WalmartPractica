    // BORRAR
    private void eliminarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_eliminarMousePressed

        int idcell = jTable1.getSelectedRow();
        if (idcell <= -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe seleccionar un producto a borrar. \n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        } else {
            List<Map<String, Object>> datos = dbManager.listar(TABLA);
            String id = datos.get(idcell).get("idProducto").toString();

            int aceptar = javax.swing.JOptionPane.showConfirmDialog(this, "¬øDesea eliminar este producto?", "ELIMINAR", javax.swing.JOptionPane.WARNING_MESSAGE);
            if (aceptar == javax.swing.JOptionPane.YES_OPTION) {
                Map<String, Object> where = new HashMap<>();
                where.put("idProducto", id);

                dbManager.eliminar(TABLA, where);
                javax.swing.JOptionPane.showMessageDialog(this, "¬°Producto borrado! \n", "HECHO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_eliminarMousePressed

